<div class="text">
John Kutsch helped facilitate my on-a-budget attendance of <a href="http://www.thoriumenergyalliance.com/ThoriumSite/TEAC3.html">Thorium Energy Alliance 3</a> (in Washington, DC). TEAC3 was a 1 day conference, right next to the United States Capitol Complex. And the Library of Congress. The White House. Capitol Hill. 
</div>

<div class="space">&nbsp;</div>

<div class="text">
The conference was fantastic. One day I hope to return, to see those other things. 
</div>

<div class="space">&nbsp;</div>

<div class="text">
Kirk Sorensen announced the launch of Flibe Energy, and expanded his talk on medical uses of molybdenum-99, and LFTR's chemical processes.
</div>

<div class="space">&nbsp;</div>

<div class="video">
<iframe width="640" height="390" src="http://www.youtube.com/embed/6-uxvSVIGtU" frameborder="0" allowfullscreen></iframe>
</div>

<div class="space">&nbsp;</div>

<div class="text">
Robert Hargraves positioned Liquid Fluoride Thorium Reactors as a means of addressing world poverty, given they can produce energy cheaper than coal.  
</div>

<div class="space">&nbsp;</div>

<div class="video">
<iframe width="640" height="390" src="http://www.youtube.com/embed/BOoBTufkEog" frameborder="0" allowfullscreen></iframe>
</div>

<div class="space">&nbsp;</div>

<div class="text">
James Kennedy delved into the heavy-rare-earths aspect of thorium. Anyone concerned about North America losing its high-tech manufacturing base needs to watch this.
</div>

<div class="space">&nbsp;</div>

<div class="video">
<iframe width="640" height="390" src="http://www.youtube.com/embed/mrDeB86YpV4" frameborder="0" allowfullscreen></iframe>
</div>

<div class="space">&nbsp;</div>

<div class="text">
COL Paul E. Roege asked if (portable) nuclear power could help save American lives currently at risk defending military supply lines. And could that nuclear power source be left behind after military operations, to provide civilian power? 
</div>

<div class="space">&nbsp;</div>

<div class="video">
<iframe width="640" height="390" src="http://www.youtube.com/embed/a1Bk_07gaSs" frameborder="0" allowfullscreen></iframe>
</div>

<div class="space">&nbsp;</div>

<div class="text">
Alexander Cannara listed off the challenges presented by climate change, and positioned LFTR as a key part of the solution. Alex also cautioned against betting low-density sources of energy can meet our energy needs fast enough.
</div>

<div class="space">&nbsp;</div>

<div class="video">
<iframe width="640" height="390" src="http://www.youtube.com/embed/aUVq81kBKyk" frameborder="0" allowfullscreen></iframe>
