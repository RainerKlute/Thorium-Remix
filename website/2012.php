<div class="text">
TR2011 is currently seen ~1000/day on YouTube. It <a href="http://kunaki.com/sales.asp?PID=PX00ZG2ZI8&pp=1">circulates in public libraries</a>. It occasionally airs on community access TV stations. <strong>That's not nearly good enough</strong> to change public policy. A <strong>better</strong>, <strong>more viral</strong> video is needed. Let's call <a href="http://www.youtube.com/watch?v=WWUeBSoEnRk">TR2009</a>, <a href="http://www.youtube.com/watch?v=P9M__yYbsZ4">TR2011</a> (and my work on <a href="http://www.ted.com/talks/kirk_sorensen_thorium_an_alternative_nuclear_fuel.html">Kirk's TEDxYYC Talk</a>) progressively-<strong>better</strong>, extremely <strong>popular</strong> <a href="http://energyfromthorium.com/2011/06/04/adventures-with-gordon/">learning experiences</a>! How must THORIUM REMIX be improved?
<ul>
<li style="padding-right:10px;">Can <strong>not</strong> consist primarily of lecture footage. This is a show-stopper for most digitual distribution networks. Basically, if your lecture documentary <a href="http://en.wikipedia.org/wiki/An_Inconvenient_Truth">does not contain Al Gore</a>, you are out of luck. Person-to-person interactions and person-to-camera interviews must be the primary mechanism for explaining LFTR.<br />&nbsp;</li>
<li style="padding-right:10px;">Must have a <strong>brilliant opening-credit sequence</strong>. I think TR2011 has a <strong>good</strong> sequence. Good enough for most purposes. But to go insanely viral, the first 10 minutes must be <strong>absolutely riveting</strong>. TR2011 was as good as I can personally make opening credits, I need to pay someone to do this right (and that includes music).<br />&nbsp;</li>
<li style="padding-right:10px;"><strong>Core educational message</strong> needs to be <stong>made more accessible</strong> to a wider audience. TR2011's educational material is mostly culled from <a href="http://www.youtube.com/watch?v=YVSmf_qmkbg">Kirk Sorensen's Calgary Protospace lecture</a>. One lecture. Kirk's first delivery of it. In a poorly-lit basement. Even having a second delivery of the same material recorded would be invaluable. And we can do much more than that.</li>
</ul> 
...I <strong>will</strong> be going to TEAC4. And I <strong>will</strong> shoot a better thorium documentary.<br /><br />

<strong>How much better is up to you.</strong>
