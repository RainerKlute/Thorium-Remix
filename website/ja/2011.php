重水炉はウランのエネルギーの約0.7％を
使います。そして

軽水炉ではウランのエネルギーの約0.5％を
使います。共に大きな損失です。

大気圧では、水は100℃で沸騰します。
これは、効果的に発電するには

十分な高温ではありません
そのため冷却水を用いる原子炉では、
70気圧以上で運転する必要があります

原子炉を作るには、圧力容器を製造しなければなりません

人々が最も心配する原子力の事故は...

圧力が失われると、300℃に保たれている水は


一気に蒸発します。その体積は約1000倍に
膨れ上がります。もし、緊急炉心冷却装置が働かなければ

原子炉の核燃料は過熱して溶融します。


これが、軽水炉の設計で起こることです。
もし、この事故が発生すれば、すべての水蒸気は

原子炉建屋の中に充満します。

現在、人類が持つ原子炉はウラン酸化物を使います。
これはセラミックのため化学的に安定です。

しかし熱をよく伝えません。
もし原子炉の圧力が失われると、つまり

冷却水が失われると、核燃料はすぐにメルトダウンし、
その中に閉じ込めていた核分裂生成物を

放出します。

そのため、現在の原子炉は緊急炉心冷却装置を
持っており、常に炉心を冷却水で満たすようになっています。

私たちはこの考え方の過ちを福島第一原子力発電所で見ました。
そこでは複数のバックアップ用ディーゼル発電機を持っていました。

そして、それぞれは非常に高い動作確率を持っていました。
しかし津波の襲来は

それらをすべてノックアウトしました。

人々はいつも、"原子力は安全ですか？"と問います。
そして最初に私が返す答えは"どの原子力ですか？"です。

原子力にはさまざまな異なる方法があります。
自動車は安全ですか？そこで私が問うことは、

"どの自動車ですか？"

私が今とは異なる原子力について学ぶ機会を得たのは幸運だ。


トリウム溶融塩炉だ。

溶融塩炉を使えば、トリウムを完全に燃やし尽くすことができます。


今、使われている典型的な軽水炉ではウランの一部しか燃やせません。

トリウム溶融塩炉は冷却水を使いません。
トリウム溶融塩炉は燃料棒を使いません。

高温で溶けたフッ化物塩（えん）を燃料としています。
フッ化物塩（えん）を溶かすのに400℃以上に加熱します。

溶融塩燃料は原子炉でエネルギーを取り出す最高の方法です。
ここに本当の魔法があります。

それは溶融塩炉を動かすのに高い圧力は不要だということです。
溶融塩炉は冷却のために水を使わないと言いました。

原子炉の内部には燃料集合体など、複雑な装置は何もありません。


メルトダウンが起こる燃料棒を使う原子炉とは異なり、
もし溶融塩炉の冷却を停止しても、このフッ化物の燃料は

メルトダウンしません。すでに溶融しているからです。

通常の運転では、付属の小さな部品が大きな役割を果たします。
炉心の下にあるパイプの外側のファンです。

普段はこのファンで燃料塩を凍らせ、パイプを閉じています。
もし緊急事態が発生し、発電所のすべての電源を失った場合には、

この小さなファンは停止し、凍った塩は崩壊熱で溶けます。


そして炉心の燃料塩は重力だけで自動的に


炉心株のドレインタンクに排出されます。


軽水炉では、通常、炉心の崩壊熱の除去のために、
冷却水を流動させるポンプを動かすための電力が必要です。

そうでなければ燃料棒はメルトダウンします。
しかし、もしトリウム溶融塩炉で電源を失った場合、それは

人の介入なしで完全に自分自身でシャットダウンします。

驚くほど高い安全性です。
溶融塩炉に物理的な損傷が生じる場合においてもです。

トリウムは天然に存在する核燃料です。
それはウランの4倍ほどの量です。

トリウムのエネルギー密度は極めて高いため、
あなたが生きている間に必要とするすべてのエネルギーを

手のひらサイズのトリウムだけで供給できます。
ウランを利用するよりも200倍も効果的にエネルギーを得ることができます。

それは、トリウム溶融塩炉ならほとんど完全にエネルギーを取り出せるからです。


このことは、放射性廃棄物の発生量を減らすことにもつながります。
その量はウラン軽水炉の数百分の１です。

化石燃料に比べれば何百万分の１になります。

もちろん、自動車や産業機械には今後もガソリンなどの燃料が必要です。
しかし、そのような燃料も、

大気中の二酸化炭素と水から作り出すことができます。


たとえば水素は水を電気分解して得ることができます。


炭素は大気中の二酸化炭素から得ます。
その水素と炭素からメタノールやアンモニア、ジメチルエーテルのような

燃料を作ることができます。
これらは今使っているディーゼル燃料と置き換えることができます。

カーボン・ニュートラルなガソリンと軽油があると想像してみて下さい。
それは持続可能で自己完結的な流れです。

あなたはウラン235と銀やプラチナが同等であることがわかると思います。
あなたは、プラチナをエネルギーのために燃やすことを想像できますか？

しかし、現在、私たちが原子力発電所で行っていることは、
そのようなことなのです。

私たちはこの大変、希少価値の高いウラン235を燃やしています。
そしてトリウムは燃やしていないのです。

環境保護を訴える方々の中には、
「聞いてください。原子力は持続可能ではありません」

とおっしゃいます。ウランはもうすぐ不足するでしょう。
私たちは「そうですね。その通りだとお思います。もし

私たちが今、使っている原子力の話をしているのでしたら。
2007年には、世界全体で50億トンの石炭を使いました。

310億バレルの石油を使いました。そして5兆立方メートルの
天然ガスを使いました。ウランはわずか6万5千トンです。

これが世界全体のエネルギーを生み出すのに使われた燃料です。

私にはミズーリ州でレアアース鉱山を開発しようとしている友人がいます。
彼に「ジム、どのくらいのトリウムが

一年間に出てくるだろう？」と聞きました。
彼は「だいたい5千トンぐらいだと思います」と答えました。

5千トンのトリウムで世界全体が必要とするエネルギーの
1年分を供給することができます。さらにジムは言いました。

「私の鉱山と同じようにレアアースと一緒にトリウムが生まれる
場所は、他にも無数にあります。

それは素晴らしい鉱山です。それは特別ではありません。
地球上のどこでレアアース鉱山を探そうと、ここと同じことがわかります」

人類はいつの時代も、新しいエネルギー源を探そうとしてきました。
そして、新たに見出されたエネルギー源は、

社会に実装されてきました。何千年もの昔、人類は
奴隷を持っていました。

人類が炭素を奴隷にする方法を学んだ時、
人間の代わりに炭素を使うようになりました。そして、

文明人になることができました。

トリウムは、炭化水素が数百万倍のエネルギー密度を持っています。


これは人類の文明化にとって何を意味しますか？
なぜならこのトリウムが不足することはないのですから。

トリウムが不足することはありません。これはとてもシンプルなことです。

私たちのFacebookのページを見ると、性別の比率は
約87％が男性で、13％が女性です。
