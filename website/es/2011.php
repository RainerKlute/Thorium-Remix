En un reactor de agua pesada se aprovecha alrededor de 0,7%
de la energía contenida en el uranio, y

en un reactor de agua ligera se aprovecha alrededor de un 0,5%
de su energía. Ambos son muy poco eficientes.

A presiones normales, el agua hierve a 100 grados centígrados. Esto no es suficiente para generar electricidad con eficacia

Por lo tanto, los reactores refrigerados por agua tienen que funcionar a más de 70 atmósferas de presión.

Por tanto, es necesario introducir el reactor en una vasija a presión.

La principal preocupación sería una accidente ...

en el que se perdiera la presión a 300 grados celsius

ya que el vapor saldría disparado. Su volumen aumentaría aproximadamente
por un factor de 1.000. Si no se fuera capaz introducir

refrigerante al combustible del reactor, éste puede llegar a
sobrecalentarse y fundirse.

Esto es lo que justifica el diseño de este edificio.
Si esto ocurriera, todo el vapor quedaría confinado en este edificio.

Los reactores que tenemos hoy en día utilizan óxido de uranio
como combustible. Se trata de un material cerámico, químicamente

estable, pero no muy bueno transfiriendo calor.
Si se perdiera la presión, se perdería el

agua en estado líquido y, al poco tiempo, el combustible se derretiría y
se liberarían productos radioactivos procedentes de la fisión a la misma.

Por lo que disponen de una serie de sistemas de emergencia
diseñados para mantener siempre el núcleo sumergido en agua.

Hemos visto el fallo de este sistema en Fukushima
Daiichi. Se disponía de varios generadores diesel redundantes,

y existía una elevada probabilidad de que cada uno de ellos arrancara. Cuando el tsunami llegó a tierra los inutilizó todos

La gente suele preguntar: "¿Es segura la energía nuclear?"
y lo primero que les respondo es: "¿Cuál de ellas?"

Hay miles de maneras de utilizar la energía nuclear. ¿Es un seguro un coche? Bueno… ¿cuál de ellos?

Tuve la suerte de poder estudiar una forma diferente
de energía nuclear.

El reactor de torio en sales de flúor en estado líquido (LFTR).

En este tipo reactor podemos llegar a quemar completamente el torio
frente a sólo una pequeña parte del uranio en un reactor convencional de agua ligera.

No se utiliza agua para refrigerarlo ni tampoco se utilizan
combustibles sólidos. Se basa en utilizar sales de flúor

como combustible nuclear. Es preciso alcanzar 400 grados Celsius
para que llegue a fundirse,

pero, en cambio, resulta ideal
para generar energía eléctrica en un reactor nuclear. En ello reside

la verdadera "magia" - no se tiene que alcanzar una
elevada presión. Tampoco es necesario utilizar agua

como refrigerante y no hay nada en el reactor
que pueda cambiar bruscamente de densidad.

A diferencia de los combustibles sólidos, que pueden fundirse
si se dejan de refrigerar, en estos de sales de flúor en estado líquido

el combustible ya está fundido.

En un funcionamiento normal, se dispone de un pequeño tapón
de sal en estado sólido que mantiene su estado

aplicando gas frío sobre el exterior de la tubería que lo contiene.
En caso de emergencia y de pérdida de

energía eléctrica en la planta de energía nuclear,
el ventilador deja de soplar, y el tapón de sal solidificada

se derrite, y el combustible líquido de sales de flúor

se evacúa del interior del reactor

a través de un conducto a otro contenedor, llamado
tanque de desagüe.

En los reactores refrigerados por agua, por lo general, es preciso
disponer de energía eléctrica en la planta para mantener

el agua en circulación y para evitar la fusión del núcleo.
Pero si un LFTR pierde el suministro eléctrico, se apaga

por sí mismo, sin intervención humana.

Proporciona un nivel asombroso de seguridad,
incluso en el caso de que se produjeran daños físicos en el reactor.

El torio es un combustible nuclear de origen natural
cuatro veces más común que el uranio en la corteza terrestre

Tiene una densidad energética tal que con el torio que cabe en una mano se pueden satisfacer las necesidades de energía de una persona de por vida

Podemos hacer un uso del torio 200 veces más eficiente
que el que estamos obteniendo actualmente por el uranio

Dado que el LFTR es capaz de liberar casi en su totalidad
la energía contenida en el torio

lo que reduce los residuos que actualmente genera la explotación del uranio en un factor de cientos de veces,

y por factores de millones en el caso de los combustibles fósiles.

Seguiremos necesitando combustibles líquidos para
vehículos y maquinaria, pero estos

se pueden generar a partir del dióxido de carbono
de la atmósfera y del agua, tal y como

hace la naturaleza. Podríamos generar hidrógeno por
separación del agua y combinarlo con el carbono

procedente del CO2 de la atmósfera.
Combustibles como el metanol, el amoníaco y dimetil éter

podrían reemplazar de forma directa
a la gasolina y el gasóleo en motores de combustión interna.

Imagine que fuera posible disponer de gasolina y gasóleo sin aportar carbono a la atmósfera,
de modo sostenible y de fabricación propia.

En esta tabla puede verse que el uranio-235 es tan escaso como
la plata y el platino. ¿Se imagina quemar platino para obtener energía?

Eso es lo que estamos haciendo con la energía nuclear hoy en día,

estamos quemando estos elementos tan escasos, en lugar
de quemar el abundante torio disponible.

Algunas personas que se consideran ecologistas dicen:
"la energía nuclear no es sostenible,

nos vamos a quedar sin uranio".
De acuerdo, les doy la razón, si estamos hablando

de la tecnología nuclear que utilizamos hoy en día.
En 2007 se utilizaron 5.000 millones de toneladas de carbón,

31.000 millones de barriles de petróleo, y 5 billones de
metros cúbicos de gas natural, junto con 65.000 toneladas

de uranio para producir energía en el mundo.

Tengo un amigo que está tratando de explotar mina de
tierras raras en Missouri. "Jim, ¿cuánto torio

crees que podrás extraer en un año? "
Y me explica: "Creo que alrededor de 5.000 toneladas".

5.000 toneladas de torio son suficientes para suministrar toda la energía
que utiliza el planeta en un año. Y continúa explicandóme:

"Hay un trillón de otros sitios en la tierra similares a mi mina.

Mi mina es un buen yacimiento, pero no es extraordinaria, no es el único lugar en el
mundo donde se puede encontrar un yacimiento como este"

Cada vez que la humanidad ha sido capaz de acceder a
una nueva fuente de energía, se han producido profundos

cambios sociales. Los seres humanos han tenido esclavos
durante miles y miles de años.

Cuando hemos aprendido cómo esclavizar al carbono, en lugar
de a otros seres humanos, hemos comenzado a aprender

a ser personas civilizadas.

El torio tiene un millón de veces la densidad de energía
de un enlace carbono-hidrógeno.

¿Qué podría ello significar para la civilización humana?
Debido a que es muy abundante, nunca vamos a tener escasez de este elemento.

Simplemente, es demasiado común.

