Thorium, converted into energy in a Liquid Fluoride Thorium Reactor (LFTR pronounced "Lifter"), has the potential to completely eliminate our dependence on fossil fuel based electricity generation... All the while releasing orders of magnitude <strong>less radioactive material</strong> into the atmosphere than coal and natural gas! <strong>No greenhouse gasses</strong> are emitted. And <strong>operating costs</strong> are projected to easily <strong>compete with coal</strong>.
</div>

